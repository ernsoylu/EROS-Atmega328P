#!/usr/bin/env python3
"""erosgen - EROS system configurator (the "OIL file" compiler).

Reads one app.yaml per application and generates the static OS
configuration (config.h / config.c), the application Makefile, and -
for files that do not exist yet - per-rate ASW skeletons and a main.c
integration skeleton. Only peripherals listed in the YAML are compiled
in; buffer geometry (UART rings, pool arena) is set from the YAML so
RAM is spent deliberately.

Usage:
    python3 tools/erosgen.py <app.yaml> [--check]

    --check   validate + report only, write nothing

Design rules encoded here (see README.md / codegen/README.md):
  * TaskID == static priority == ready-mask bit; max 8 tasks.
  * Priorities are assigned rate-monotonically: fastest period =
    highest priority; aperiodic (activated/chained) tasks sit below
    the periodic set; the autostart init task is lowest.
  * One cyclic alarm per periodic task, IDs ordered fastest first.
  * Resource ceiling = highest-priority user (computed, not typed).
  * Schedulability gate: sum of all periodic WCETs must fit in the
    base (fastest) period - blocking, never corruption, but enforced.
"""

import difflib
import math
import os
import sys
from pathlib import Path

try:
    import yaml
except ImportError:  # pragma: no cover
    sys.exit("erosgen: PyYAML required (pip install pyyaml)")

GENERATED_BANNER = "GENERATED by tools/erosgen.py from {src} - DO NOT EDIT"

# Driver name -> source file. Resolution order: application directory
# first (app-local driver like the demo's uart.c), then drivers_dir.
KNOWN_PERIPHERALS = {
    "uart":       "uart.c",
    "pwm":        "pwm.c",        # Timer1 PWM
    "adc":        "adc.c",
    "eeprom":     "eeprom.c",
    "i2c":        "i2c.c",
    "spi":        "spi.c",
    "extint":     "extint.c",
    "timer0_pwm": "timer0_pwm.c",
    "icp":        "icp.c",
    "acomp":      "acomp.c",
}

# Pins each peripheral drives when enabled - the single source of truth
# for conflict detection (replaces hardcoded peripheral-pair lists).
# Timer2 is the kernel tick and owns no application pins. ADC channels
# A0..A5 are only claimed if the app lists them (see peripheral config),
# so plain ADC declares nothing here.
PERIPHERAL_PINS = {
    "uart":       ["PD0", "PD1"],          # RXD, TXD
    "pwm":        ["PB1"],                 # OC1A
    "i2c":        ["PC4", "PC5"],          # SDA, SCL
    "spi":        ["PB2", "PB3", "PB4", "PB5"],  # SS, MOSI, MISO, SCK
    "timer0_pwm": ["PD5", "PD6"],          # OC0B, OC0A
    "icp":        ["PB0"],                 # ICP1
    "acomp":      ["PD7"],                 # AIN1 (AIN0/PD6 added if external)
    "extint":     [],                      # pins are app-assigned
    "eeprom":     [],
    "adc":        [],
}

# Shared-resource conflicts that are not expressible as pin overlap
# (two peripherals contending for the same timer/hardware block).
CONFLICTS_HARD = [
    ("icp", "pwm", "both own Timer1 (capture vs ICR1-as-TOP)"),
]

# Recognised keys per YAML section - anything else is a typo and is
# rejected (with a "did you mean" hint). This is the highest-value
# guard: a misspelled 'period_ms' would otherwise silently make a task
# aperiodic with no error.
ALLOWED_KEYS = {
    "doc":        {"system", "sources", "peripherals", "tasks",
                   "resources", "pool", "gpio", "simulink"},
    "system":     {"name", "kernel_dir", "drivers_dir", "tick_hz",
                   "alarm_max_offset", "stack", "hooks", "budget"},
    "stack":      {"canary", "guard_bytes", "paint_margin"},
    "hooks":      {"startup", "error", "shutdown"},
    "budget":     {"flash", "ram", "sram_total"},
    "task":       {"name", "entry", "period_ms", "wcet_ms", "autostart",
                   "watchdog", "runnables"},
    "resource":   {"name", "users", "mask_tick_isr"},
    "pool":       {"block_size", "blocks"},
    "gpio":       {"pin", "dir", "pullup", "name", "init"},
    "simulink":   {"model", "dir", "rate_map"},
    "uart":       {"baud", "tx_ring", "rx_ring"},
}


class ConfigError(Exception):
    pass


def fail(msg):
    raise ConfigError("erosgen: " + msg)


def check_keys(d, section, where):
    """Reject unknown keys in a YAML mapping, suggesting the closest
    valid key. `section` selects the allowed-key set; `where` names the
    location for the error message."""
    if not isinstance(d, dict):
        fail(f"{where}: expected a mapping, got {type(d).__name__}")
    allowed = ALLOWED_KEYS[section]
    for k in d:
        if k not in allowed:
            hint = difflib.get_close_matches(str(k), allowed, n=1)
            suffix = f" (did you mean '{hint[0]}'?)" if hint else \
                     f" (valid: {', '.join(sorted(allowed))})"
            fail(f"{where}: unknown key '{k}'{suffix}")


def is_pow2(n):
    return n >= 1 and (n & (n - 1)) == 0


# Arduino Nano silk -> ATmega328P port pin. Accept either form in YAML.
NANO_ALIASES = {}
for _d, _pp in {0: "PD0", 1: "PD1", 2: "PD2", 3: "PD3", 4: "PD4",
                5: "PD5", 6: "PD6", 7: "PD7", 8: "PB0", 9: "PB1",
                10: "PB2", 11: "PB3", 12: "PB4", 13: "PB5"}.items():
    NANO_ALIASES[f"D{_d}"] = _pp
for _a in range(6):  # A0..A5 double as PC0..PC5; A6/A7 are ADC-only
    NANO_ALIASES[f"A{_a}"] = f"PC{_a}"


def normalize_pin(name):
    """Return a canonical PORTxBIT pin name (e.g. 'PB5') from either an
    AVR name or an Arduino Nano alias ('D13', 'A4')."""
    p = str(name).upper()
    if p in NANO_ALIASES:
        return NANO_ALIASES[p]
    if (len(p) == 3 and p[0] == "P" and p[1] in "BCD" and p[2].isdigit()):
        return p
    fail(f"unknown pin '{name}' (use PB0..PD7 or Nano D0..D13 / A0..A5)")


