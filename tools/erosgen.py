def emit_asw_skeleton(s, task):
    step_call = None
    if s.simulink:
        for step, tname in (s.simulink.get("rate_map") or {}).items():
            if str(tname).upper() == task.name:
                step_call = f"{s.simulink['model']}_{step}();"
    L = []
    L.append("/**")
    L.append(f" * @file    asw_{task.period_ms}ms.c")
    L.append(f" * @brief   {task.period_ms} ms rate - TASK_{task.name}.")
    L.append(" *")
    L.append(" * Generated once by tools/erosgen.py - edit freely; it will not")
    L.append(" * be overwritten. Keep rate-local state static in this file;")
    L.append(" * cross-rate signals belong in an asw_signals module.")
    L.append(" */")
    L.append("")
    L.append('#include "eros.h"')
    if s.simulink:
        L.append(f'#include "{s.simulink["model"]}.h"')
    L.append("")
    if task.runnables:
        L.append("/* Runnables you implement (here or in another TU). Move")
        L.append(" * these prototypes to a shared header if reused. */")
        for r in task.runnables:
            L.append(f"extern void {r}(void);")
        L.append("")
    L.append(f"/** TASK_{task.name} - {task.period_ms} ms, WCET <= "
             f"{task.wcet_ticks} tick(s). */")
    L.append(f"void {task.entry}(void)")
    L.append("{")
    if step_call:
        L.append("    /* sample: hardware -> model inports here */")
        L.append(f"    {step_call}")
        L.append("    /* actuate: model outports -> hardware here */")
    for r in task.runnables:
        L.append(f"    {r}();")
    if not step_call and not task.runnables:
        L.append("    /* TODO: runnables for this rate */")
    L.append("    TerminateTask();")
    L.append("}")
    return "\n".join(L) + "\n"


# Driver name -> Init() call emitted into Board_ConfigurePins().
DRIVER_INIT = {
    "uart":       "UART_Init();",
    "pwm":        "PWM_Init();",
    "adc":        "ADC_Init();",
    "i2c":        "I2C_Init();",
    "spi":        "SPI_Init(SPI_MODE0, SPI_CLK_DIV16);",
    "timer0_pwm": "T0PWM_Init();",
    "icp":        "ICP_Init();",
    # eeprom needs no init; extint/acomp are enabled with app-specific
    # arguments, so they are left to hand-written setup.
}
DRIVER_HEADER = {
    "uart": "uart.h", "pwm": "pwm.h", "adc": "adc.h", "eeprom": "eeprom.h",
    "i2c": "i2c.h", "spi": "spi.h", "extint": "extint.h",
    "timer0_pwm": "timer0_pwm.h", "icp": "icp.h", "acomp": "acomp.h",
}


def emit_os_gen_h(s):
    """Always-regenerated glue kept in lockstep with the YAML: board pin
    setup (from gpio + peripheral inits) and alarm arming. main.c calls
    these so adding a task/pin in the YAML propagates without editing
    hand-written code (the regeneration-drift fix)."""
    L = []
    L.append("/**")
    L.append(" * @file    os_gen.h")
    L.append(f" * @brief   {GENERATED_BANNER.format(src=s.src.name)}")
    L.append(" *")
    L.append(" * Regenerated on every erosgen run - never edit. Include it")
    L.append(" * from main.c and call Board_ConfigurePins() in StartupHook()")
    L.append(" * and OS_StartAlarms() in the autostart task, so pin and alarm")
    L.append(" * wiring stay in sync with the YAML.")
    L.append(" */")
    L.append("")
    L.append("#ifndef OS_GEN_H")
    L.append("#define OS_GEN_H")
    L.append("")
    L.append("#include <avr/io.h>")
    L.append('#include "eros.h"')
    for p in sorted(s.peripherals):
        L.append(f'#include "{DRIVER_HEADER[p]}"')
    L.append("")
    L.append("/** Pin directions/pull-ups + enabled-driver init. Call from")
    L.append(" *  StartupHook() (interrupts still disabled). */")
    L.append("static inline void Board_ConfigurePins(void)")
    L.append("{")
    # GPIO directions grouped by port for compactness.
    for g in s.gpio:
        port = g["pin"][1]      # B/C/D
        bit = g["pin"]          # e.g. PB5 - avr-libc defines the bit index
        if g["dir"] == "out":
            L.append(f"    DDR{port} |= (uint8_t)(1u << {bit});"
                     f"  /* {g['name'] or g['pin']} output */")
            if g["init"]:
                L.append(f"    PORT{port} |= (uint8_t)(1u << {bit});")
            else:
                L.append(f"    PORT{port} &= (uint8_t)~(1u << {bit});")
        else:
            L.append(f"    DDR{port} &= (uint8_t)~(1u << {bit});"
                     f"  /* {g['name'] or g['pin']} input */")
            if g["pullup"]:
                L.append(f"    PORT{port} |= (uint8_t)(1u << {bit});"
                         "  /* pull-up */")
    for p in sorted(s.peripherals):
        if p in DRIVER_INIT:
            L.append(f"    {DRIVER_INIT[p]}")
    if not s.gpio and not any(p in DRIVER_INIT for p in s.peripherals):
        L.append("    /* no gpio or auto-init drivers configured */")
    L.append("}")
    L.append("")
    L.append("/** Arm every cyclic alarm with ALIGNED releases (all first")
    L.append(" *  expiries at the base period so shared release points")
    L.append(" *  coincide). Call from the autostart task. */")
    L.append("static inline void OS_StartAlarms(void)")
    L.append("{")
    base = s.periodic[0].period_ticks
    for t in s.periodic:
        L.append(f"    (void)SetRelAlarm(ALARM_{t.name}, {base}u, "
                 f"TASK_{t.name}_PERIOD_TICKS);")
    L.append("}")
    L.append("")
    L.append("#endif /* OS_GEN_H */")
    return "\n".join(L) + "\n"


def emit_main_skeleton(s):
    init = next((t for t in s.tasks if t.autostart), None)
    L = []
    L.append("/**")
    L.append(" * @file    main.c")
    L.append(" * @brief   Integration layer: hooks + init task + main().")
    L.append(" *")
    L.append(" * Generated once by tools/erosgen.py - edit freely. Pin setup")
    L.append(" * and alarm arming live in os_gen.h (regenerated every run).")
    L.append(" */")
    L.append("")
    L.append('#include "eros.h"')
    L.append('#include "os_gen.h"')
    L.append("")
    if s.hook_startup:
        L.append("void StartupHook(void)")
        L.append("{")
        L.append("    Board_ConfigurePins(); /* generated: gpio + driver init */")
        L.append("}")
        L.append("")
    if s.hook_error:
        L.append("void ErrorHook(StatusType error)")
        L.append("{")
        L.append("    (void)error; /* may run in tick-ISR context: stay tiny */")
        L.append("}")
        L.append("")
    if s.hook_shutdown:
        L.append("void ShutdownHook(StatusType error)")
        L.append("{")
        L.append("    (void)error; /* terminal fault tombstone */")
        L.append("}")
        L.append("")
    if init is not None:
        L.append(f"/** TASK_{init.name} - autostart: arm the cyclic alarms. */")
        L.append(f"void {init.entry}(void)")
        L.append("{")
        if s.simulink:
            L.append(f"    {s.simulink['model']}_initialize();")
        L.append("    OS_StartAlarms(); /* generated from the YAML task set */")
        L.append("    TerminateTask();")
        L.append("}")
        L.append("")
    L.append("int main(void)")
    L.append("{")
    L.append("    StartOS(); /* noreturn */")
    L.append("}")
    return "\n".join(L) + "\n"


