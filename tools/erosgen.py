def emit_config_h(s):
    L = []
    L.append("/**")
    L.append(" * @file    config.h")
    L.append(f" * @brief   {GENERATED_BANNER.format(src=s.src.name)}")
    L.append(" *")
    L.append(" * EROS static application configuration (OSEK \"OIL\" "
             "equivalent).")
    L.append(" * Edit " + s.src.name + " and regenerate; see tools/erosgen.py.")
    L.append(" */")
    L.append("")
    L.append("#ifndef EROS_CONFIG_H")
    L.append("#define EROS_CONFIG_H")
    L.append("")
    L.append('#include "eros_types.h"')
    L.append("")
    L.append("/* ---- system counter --------------------------------------------- */")
    L.append(f"#define OS_TICK_HZ            {s.tick_hz}u")
    L.append(f"#define OS_TICK_MS            {s.tick_ms}u")
    L.append(f"#define OS_ALARM_MAX_OFFSET   {s.alarm_max_offset}u")
    L.append("")
    L.append("/* ---- tasks: TaskID == priority == ready-mask bit ----------------- */")
    L.append("/* WCET budgets (ticks, monitored at +/-1 tick):                      */")
    for t in s.tasks_by_prio:
        per = f"{t.period_ms} ms" if t.period_ms else \
              ("autostart, one-shot" if t.autostart else "activated/chained")
        L.append(f"/*   TASK_{t.name:<10} prio {t.priority}  wcet <= "
                 f"{t.wcet_ticks} tick(s)  ({per}) */")
    L.append(f"#define OS_NUM_TASKS          {len(s.tasks)}u")
    L.append("")
    for t in s.tasks_by_prio:
        L.append(f"#define TASK_{t.name:<16} ((TaskType){t.priority}u)")
    L.append("")
    for t in s.tasks_by_prio:
        L.append(f"extern void {t.entry}(void);")
    L.append("")
    L.append("/* ---- alarms: one cyclic alarm per periodic task ------------------ */")
    L.append(f"#define OS_NUM_ALARMS         {len(s.periodic)}u")
    L.append("")
    for i, t in enumerate(s.periodic):
        L.append(f"#define ALARM_{t.name:<15} ((AlarmType){i}u) "
                 f"/* {t.period_ms} ms -> TASK_{t.name} */")
    L.append("")
    L.append("/* Period macros for SetRelAlarm/SetAbsAlarm calls in the app. */")
    for t in s.periodic:
        L.append(f"#define TASK_{t.name}_PERIOD_TICKS {t.period_ticks}u")
    L.append("")
    L.append("/* ---- resources (IPCP; ceiling = highest-priority user) ---------- */")
    L.append(f"#define OS_NUM_RESOURCES      {len(s.resources)}u")
    L.append("")
    for i, r in enumerate(s.resources):
        users = ", ".join(t.name for t in r.users)
        L.append(f"#define RES_{r.name:<17} ((ResourceType){i}u) "
                 f"/* users: {users} */")
    L.append("")
    L.append("/* ---- fixed-block pool -------------------------------------------- */")
    L.append(f"#define OS_POOL_BLOCK_SIZE    {s.pool_block}u")
    L.append(f"#define OS_POOL_NUM_BLOCKS    {s.pool_blocks}u")
    L.append("")
    L.append("/* ---- watchdog aliveness ------------------------------------------ */")
    if s.alive_tasks:
        parts = " | ".join(f"(1u << TASK_{t.name})" for t in s.alive_tasks)
        L.append("#define OS_ALIVE_REQUIRED_MASK \\")
        L.append(f"    ((uint8_t)({parts}))")
    else:
        L.append("#define OS_ALIVE_REQUIRED_MASK ((uint8_t)0u)")
    L.append("")
    L.append("/* ---- hooks -------------------------------------------------------- */")
    L.append(f"#define OS_CFG_STARTUPHOOK    {s.hook_startup}")
    L.append(f"#define OS_CFG_ERRORHOOK      {s.hook_error}")
    L.append(f"#define OS_CFG_SHUTDOWNHOOK   {s.hook_shutdown}")
    L.append("")
    L.append("/* ---- stack monitoring --------------------------------------------- */")
    L.append(f"#define OS_STACK_CANARY        0x{s.stack_canary:02X}u")
    L.append(f"#define OS_STACK_GUARD_BYTES   {s.stack_guard}u")
    L.append(f"#define OS_STACK_PAINT_MARGIN  {s.stack_margin}u")
    L.append("")
    L.append("/* ---- compile-time validation -------------------------------------- */")
    L.append('OS_STATIC_ASSERT(OS_NUM_TASKS >= 1u, "at least one task required");')
    L.append('OS_STATIC_ASSERT(OS_NUM_TASKS <= 8u, "ready queue is an 8-bit mask: max 8 tasks");')
    L.append("")
    ors = " | ".join(f"(1u << TASK_{t.name})" for t in s.tasks_by_prio)
    L.append("OS_STATIC_ASSERT(")
    L.append(f"    ({ors}) ==")
    L.append("    ((1u << OS_NUM_TASKS) - 1u),")
    L.append('    "task IDs/priorities must be unique bit positions 0..OS_NUM_TASKS-1");')
    L.append("")
    L.append('OS_STATIC_ASSERT(OS_NUM_ALARMS >= 1u, "at least one alarm required");')
    checks = " && ".join(f"(ALARM_{t.name} < OS_NUM_ALARMS)" for t in s.periodic)
    L.append(f"OS_STATIC_ASSERT({checks},")
    L.append('                 "alarm ID out of range");')
    L.append("")
    L.append('OS_STATIC_ASSERT(OS_NUM_RESOURCES <= 8u, "resource held-mask is 8-bit");')
    rchecks = " && ".join(f"(RES_{r.name} < OS_NUM_RESOURCES)" for r in s.resources)
    L.append(f"OS_STATIC_ASSERT({rchecks},")
    L.append('                 "resource ID out of range");')
    L.append("")
    L.append('OS_STATIC_ASSERT((OS_POOL_NUM_BLOCKS >= 1u) && (OS_POOL_NUM_BLOCKS <= 8u),')
    L.append('                 "pool allocation bitmask is 8-bit: 1..8 blocks");')
    L.append('OS_STATIC_ASSERT(OS_POOL_BLOCK_SIZE >= 1u,')
    L.append('                 "free-list link needs one byte per block");')
    L.append("")
    L.append('OS_STATIC_ASSERT((OS_ALIVE_REQUIRED_MASK & ~((1u << OS_NUM_TASKS) - 1u)) == 0u,')
    L.append('                 "aliveness mask references a non-existent task");')
    L.append("")
    L.append("#endif /* EROS_CONFIG_H */")
    return "\n".join(L) + "\n"


def emit_config_c(s):
    L = []
    L.append("/**")
    L.append(" * @file    config.c")
    L.append(f" * @brief   {GENERATED_BANNER.format(src=s.src.name)}")
    L.append(" *")
    L.append(" * Const configuration tables in PROGMEM; the pool arena is the")
    L.append(" * only RAM contributed here (user payload, reported separately")
    L.append(" * from the kernel RAM budget).")
    L.append(" */")
    L.append("")
    L.append("#include <avr/pgmspace.h>")
    L.append("")
    L.append('#include "eros.h"')
    L.append("")
    L.append("const OsTaskConfigType OS_taskConfig[OS_NUM_TASKS] PROGMEM =")
    L.append("{")
    for t in s.tasks_by_prio:
        L.append(f"    [TASK_{t.name}] = {{ {t.entry}, "
                 f"{1 if t.autostart else 0}u /* autostart */, "
                 f"{t.wcet_ticks}u /* WCET ticks */ }},")
    L.append("};")
    L.append("")
    L.append("const OsAlarmConfigType OS_alarmConfig[OS_NUM_ALARMS] PROGMEM =")
    L.append("{")
    for t in s.periodic:
        L.append(f"    [ALARM_{t.name}] = {{ TASK_{t.name} }},")
    L.append("};")
    L.append("")
    L.append("const OsResourceConfigType OS_resourceConfig[OS_NUM_RESOURCES] PROGMEM =")
    L.append("{")
    for r in s.resources:
        L.append(f"    [RES_{r.name}] = {{ TASK_{r.ceiling.name} /* ceiling */, "
                 f"{1 if r.mask_tick_isr else 0}u /* mask tick ISR */ }},")
    L.append("};")
    L.append("")
    L.append("uint8_t OS_poolArena[(uint16_t)OS_POOL_NUM_BLOCKS * OS_POOL_BLOCK_SIZE];")
    return "\n".join(L) + "\n"


def driver_sources(s, app_dir):
    """Resolve peripheral names to source files: app dir wins, then
    drivers_dir (added to VPATH)."""
    local, external = [], []
    for p in sorted(s.peripherals):
        fname = KNOWN_PERIPHERALS[p]
        if (app_dir / fname).exists():
            local.append(fname)
        elif s.drivers_dir and (app_dir / s.drivers_dir / fname).exists():
            external.append(fname)
        else:
            fail(f"peripheral '{p}': source {fname} not found in app dir"
                 + (f" or {s.drivers_dir}" if s.drivers_dir else
                    " (set system.drivers_dir)"))
    return local, external


def periph_defines(s):
    defs = []
    uart = s.peripherals.get("uart")
    if uart is not None:
        uart = uart or {}
        defs.append(f"-DUART_BAUD={int(uart.get('baud', 9600))}UL")
        defs.append(f"-DUART_TX_SIZE={int(uart.get('tx_ring', 128))}u")
        defs.append(f"-DUART_RX_SIZE={int(uart.get('rx_ring', 64))}u")
    return defs


def emit_makefile(s, app_dir):
    local_drv, ext_drv = driver_sources(s, app_dir)
    # Auto-include the generated per-rate ASW files so a fresh project
    # builds without hand-editing sources; dedup against any the user
    # listed explicitly (the reference demos list them for readability).
    asw_files = [f"asw_{t.period_ms}ms.c" for t in s.periodic]
    app_srcs = list(s.sources)
    for f in asw_files + local_drv:
        if f not in app_srcs:
            app_srcs.append(f)
    vpath = [s.kernel_dir]
    if ext_drv:
        vpath.append(s.drivers_dir)
    incs = ["-I.", f"-I{s.kernel_dir}"]
    if ext_drv:
        incs.append(f"-I{s.drivers_dir}")

    model_block = []
    if s.simulink:
        mdir = s.simulink.get("dir", "../codegen")
        model = s.simulink["model"]
        model_block = [
            f"MODEL_DIR  := {mdir}/{model}_ert_rtw",
            "MODEL_SRCS := $(filter-out ert_main.c,"
            "$(notdir $(wildcard $(MODEL_DIR)/*.c)))",
        ]
        vpath.append(f"{mdir} $(MODEL_DIR)")
        incs.append(f"-I{mdir} -I$(MODEL_DIR)")

    defs = periph_defines(s)

    L = []
    L.append("# =====================================================================")
    L.append(f"# {GENERATED_BANNER.format(src=s.src.name)}")
    L.append("#")
    L.append(f"# Application '{s.name}' on the EROS kernel. Only the peripherals")
    L.append("# selected in the YAML are compiled; everything else costs 0 bytes.")
    L.append("# =====================================================================")
    L.append("")
    L.append("MCU     := atmega328p")
    L.append("F_CPU   := 16000000UL")
    L.append(f"TARGET  := {s.name}")
    L.append("")
    L.extend(model_block)
    L.append(f"VPATH   := {' '.join(vpath)}")
    L.append("")
    L.append(f"APP_SRCS := {' '.join(app_srcs)}")
    srcs = "$(APP_SRCS) " + " ".join(ext_drv + ["eros.c", "config.c"])
    if s.simulink:
        srcs += " $(MODEL_SRCS)"
    L.append(f"SRCS     := {srcs}")
    L.append("OBJS     := $(SRCS:.c=.o)")
    L.append("DEPS     := $(SRCS:.c=.d)")
    L.append("")
    L.append("CC      := avr-gcc")
    L.append("OBJCOPY := avr-objcopy")
    L.append("SIZE    := avr-size")
    L.append("AVRDUDE := avrdude")
    L.append("")
    L.append("PORT    ?= /dev/ttyUSB0")
    L.append("BAUD    ?= 57600          # old-bootloader Nano; Optiboot: 115200")
    L.append("")
    if defs:
        L.append("# Peripheral geometry from app.yaml (overrides driver defaults)")
        L.append(f"PERIPH_DEFS := {' '.join(defs)}")
        defs_ref = " $(PERIPH_DEFS)"
    else:
        defs_ref = ""
    L.append("CFLAGS  := -Wall -Wextra -Werror -std=c99 -Os -flto \\")
    L.append("           -ffunction-sections -fdata-sections -fno-common \\")
    L.append(f"           -mmcu=$(MCU) -DF_CPU=$(F_CPU){defs_ref} \\")
    L.append(f"           {' '.join(incs)}")
    L.append("LDFLAGS := -Wl,--gc-sections -Wl,-Map=$(TARGET).map")
    L.append("")
    if s.budget:
        L.append("BUDGET_DIR    := build_budget")
        L.append("CFLAGS_NOLTO  := $(filter-out -flto,$(CFLAGS))")
        L.append(f"FLASH_BUDGET  := {int(s.budget.get('flash', 3072))}")
        L.append(f"RAM_BUDGET    := {int(s.budget.get('ram', 128))}")
        L.append(f"SRAM_TOTAL    := {int(s.budget.get('sram_total', 2048))}")
        L.append("")
        L.append(".PHONY: all size budget flash clean config")
        L.append("")
        L.append("all: $(TARGET).hex size budget")
    else:
        L.append(".PHONY: all size flash clean config")
        L.append("")
        L.append("all: $(TARGET).hex size")
    L.append("")
    L.append("$(TARGET).elf: $(OBJS)")
    L.append("\t$(CC) $(CFLAGS) $(LDFLAGS) -o $@ $^")
    L.append("")
    L.append("$(TARGET).hex: $(TARGET).elf")
    L.append("\t$(OBJCOPY) -O ihex -R .eeprom $< $@")
    L.append("")
    L.append("%.o: %.c")
    L.append("\t$(CC) $(CFLAGS) -MMD -MP -c -o $@ $<")
    L.append("")
    L.append("size: $(TARGET).elf")
    L.append('\t@echo "---- final image (LTO) --------------------------------------"')
    L.append("\t@$(SIZE) -B $(TARGET).elf")
    L.append("")
    if s.budget:
        L.append("$(BUDGET_DIR):")
        L.append("\tmkdir -p $(BUDGET_DIR)")
        L.append("")
        L.append("$(BUDGET_DIR)/%.o: %.c | $(BUDGET_DIR)")
        L.append("\t$(CC) $(CFLAGS_NOLTO) -MMD -MP -c -o $@ $<")
        L.append("")
        L.append("APP_BUDGET_OBJS := $(addprefix $(BUDGET_DIR)/,$(APP_SRCS:.c=.o))")
        L.append("")
        L.append("budget: $(BUDGET_DIR)/eros.o $(BUDGET_DIR)/config.o $(APP_BUDGET_OBJS)")
        L.append('\t@echo "---- kernel budget check (non-LTO reference build) ----------"')
        L.append("\t@$(SIZE) -B $(BUDGET_DIR)/eros.o $(BUDGET_DIR)/config.o \\")
        L.append("\t         $(APP_BUDGET_OBJS) | awk ' \\")
        L.append("\t  NR==2 { kflash += $$1 + $$2; kram   = $$2 + $$3 } \\")
        L.append("\t  NR==3 { kflash += $$1 + $$2; arena  = $$2 + $$3 } \\")
        L.append("\t  NR>=4 { appram += $$2 + $$3 } \\")
        L.append("\t  END { \\")
        L.append('\t    printf("kernel Flash (eros.o+config.o) : %4d / %d bytes\\n", \\')
        L.append("\t           kflash, $(FLASH_BUDGET)); \\")
        L.append('\t    printf("kernel static RAM (eros.o)     : %4d / %d bytes\\n", \\')
        L.append("\t           kram, $(RAM_BUDGET)); \\")
        L.append('\t    printf("pool arena (config.o, excluded)   : %4d bytes\\n", arena); \\')
        L.append('\t    printf("application RAM (app objects)     : %4d bytes\\n", appram); \\')
        L.append('\t    printf("stack + idle RAM (of %d total)  : %4d bytes\\n", \\')
        L.append("\t           $(SRAM_TOTAL), $(SRAM_TOTAL) - kram - arena - appram); \\")
        L.append("\t    if (kflash > $(FLASH_BUDGET) || kram > $(RAM_BUDGET)) { \\")
        L.append('\t      printf("BUDGET EXCEEDED\\n"); exit 1; \\')
        L.append("\t    } else { \\")
        L.append('\t      printf("budgets OK\\n"); \\')
        L.append("\t    } \\")
        L.append("\t  }'")
        L.append("")
    L.append("flash: $(TARGET).hex")
    L.append("\t$(AVRDUDE) -v -p m328p -c arduino -P $(PORT) -b $(BAUD) \\")
    L.append("\t           -U flash:w:$(TARGET).hex:i")
    L.append("")
    erosgen_rel = os.path.relpath(os.path.abspath(__file__), str(app_dir))
    L.append("# Regenerate config.h/config.c/Makefile/os_gen.h from the YAML.")
    L.append("# Run this after editing app.yaml (config is NOT auto-rebuilt,")
    L.append("# so a Python-less CI can still 'make' from committed output).")
    L.append("config:")
    L.append(f"\tpython3 {erosgen_rel} {s.src.name}")
    L.append("")
    L.append("clean:")
    if s.budget:
        L.append("\trm -rf $(OBJS) $(DEPS) $(BUDGET_DIR) \\")
        L.append("\t       $(TARGET).elf $(TARGET).hex $(TARGET).map")
    else:
        L.append("\trm -f $(OBJS) $(DEPS) $(TARGET).elf $(TARGET).hex $(TARGET).map")
    L.append("")
    L.append("-include $(DEPS)")
    if s.budget:
        L.append("-include $(addprefix $(BUDGET_DIR)/,$(DEPS))")
    return "\n".join(L) + "\n"


